def build_rows():
    rows = (
        build_drinks()
        + build_alcohol()
        + build_rice()
        + build_pastries()
        + build_noodles()
        + build_dumplings()
    )

    if len(rows) != 100:
        raise RuntimeError(f"總筆數應為 100，實際為 {len(rows)}")

    names = [row[1] for row in rows]
    descriptions = [row[4] for row in rows]

    if len(set(names)) != len(names):
        raise RuntimeError("存在重複菜品名稱")

    if len(set(descriptions)) != len(descriptions):
        raise RuntimeError("存在重複菜品介紹")

    return rows


